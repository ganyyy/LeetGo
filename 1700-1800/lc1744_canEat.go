func canEat(candiesCount []int, queries [][]int) []bool {
	// 感觉还是一种前缀和

	for i := 1; i < len(candiesCount); i++ {
		candiesCount[i] += candiesCount[i-1]
	}

	// 要么往大了的算, 要么往小了的算

	var ret = make([]bool, len(queries))

	for i, q := range queries {
		// 要求时 在每天吃的糖果的数量不超过 dc的情况下
		// 咋fd天吃到ft类型的糖果
		// 到达fd天时, 能吃的糖果的数量在 [(fd-1), (fd-1)*dc]
		// 相当于 candiesCount[ft] 在这个范围内即可
		var ft, fd, dc = q[0], q[1], q[2]

		// 至少要保证, 到第ft天时喜欢类型的糖果余量充足
		ret[i] = (fd + 1) <= candiesCount[ft]
		if ft != 0 {
			// 如果存在前置类型, 需要保证在fd天能吃完所有的前置
			ret[i] = ret[i] && (fd+1)*dc > candiesCount[ft-1]
		}
	}

	return ret
}

